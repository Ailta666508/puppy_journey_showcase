/**
 * 从旅行记录的 locationText 解析出可能匹配的城市 key（CITY_COORDS 的 key）
 */
export function matchCityFromLocation(locationText: string | undefined): string | null {
  if (!locationText?.trim()) return null;
  const t = locationText.trim();
  for (const city of Object.keys(CITY_COORDS)) {
    if (t === city || t.includes(city)) return city;
  }
  return null;
}
